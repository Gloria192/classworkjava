public class EvenOdd {
    public static void main(String[] args) {
        int a=50;
        for (int i=0; i<=50;i++){
            if(a%i==0){
              System.out.println("the number us Even"+a);  
            }else {
                System.out.println("the number is odd"+a);
            }
        }
       
    }
}
